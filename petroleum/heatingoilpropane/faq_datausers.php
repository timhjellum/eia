<h2>FAQs for Data Users</h2>
<h3>Q1: Why are data not collected over the summer?</h3>
<p>



The residential pricing data collected on heating oil and propane prices are for the Winter Heating Fuels Survey. The purpose of this survey is to collect prices for these fuels as they are used for heating purposes. For the purposes of the survey, the winter heating season extends from October through March. However, EIA does publish <a href="/dnav/pet/pet_pri_spt_s1_d.htm">spot prices</a> for heating oil and propane throughout the year. In addition, some State Energy Offices collect these data independent of this survey.</p>
<h3>Q2:  I am being charged more than the State average by my heating fuel provider.  Am I a victim of price-gouging?</h3>
<p>



There are many factors that affect heating fuel prices, including the volume amount delivered, the method and frequency of delivery, and the location of the dealer relative to their source for the fuel as well as the customer. These factors can vary widely given the size or infrastructure of a given State. If the State average does not align with the price you are paying, you can still use the data to look for trends in heating fuel prices in your area. </p>
<p>



Some residential consumers will be charged more than the average, while others will be charged less.  It is important to be an informed consumer, and we hope that the data help in this regard. If you are concerned about the prices you are being charged, be sure to call other local suppliers, or check in with your State Energy Office.</p>
<h3>Q3:  What benefit does this survey provide? </h3>
<p>



The Winter Heating Fuels Telephone survey arose out of the need for Congress to monitor heating fuel prices during especially cold winters and supply disruptions, such as those that occurred during the Gulf War in the early 1990s or more recently during the propane shortage which occurred during the winter of 2014. The rise of heating costs attributed to these events can have serious impacts on the cost of living for residents that rely on these fuels as their main source of heat. </p>
<p>



The State Energy Offices that collaborate with the U.S. Energy Information Administration to conduct this survey also use the aggregated data to monitor the heating fuel markets in their States as well as to develop and maintain programs which provide financial assistance for heating costs to low-income residents.</p>
<p>



These data are also frequently accessed by residential consumers of heating fuel as a way to monitor trends in heating fuel prices in their State.</p>
<h3>Q4: How are prices of No. 2 Distillate affected by the price changes of crude oil?</h3>
<p>



There is a relationship between the price of No. 2 Distillate and the price of crude oil. However, this relationship is not always straightforward. Since No. 2 Distillate is a crude oil product, it must be refined and distributed before it is ready for sale, leading to differences in the trends of the two prices. Wholesale spot prices of heating oil tend to be more closely related to crude oil prices, because they often do not account for local market conditions or distribution costs. However, in the event of a cold winter when heating oil supply is low, the price for the residential heating oil to the consumer may increase despite a decrease in the current spot price for crude oil.</p>
<h3>Q5:  Why does the price of heating fuel always appear to be so much higher in the Northeast than the Midwest?</h3>
<p>



There are a number of reasons why heating fuel prices differ in one part of the country versus another. Many local market conditions factor into heating fuel prices. For example, there may be more retailers in one market than in another with greater competition, lower prices in the market with more retailers, or the typical delivery volumes may be different with lower prices charged for higher delivery volumes. </p>
<p>



One important factor affecting retail heating fuel supplies is the distribution options for transporting these products to a given region. Some parts of the country rely on a few distribution routes to supply most of the heating fuel to the region, which means that supplies are limited by capacity. Regional factors affecting heating fuel supplies play a significant role in determining price differences for customers in the Northeast and Midwest.</p>
<h3>Q6: Why are heating oil and propane price data not collected for other States?  </h3>
<p>



The survey was developed to collect information for the States using heating oil and propane as their primary source of residential heating during the winter months. As shown in this <a href="/todayinenergy/detail.cfm?id=3550">2011 article</a>, the majority of heating oil and propane consumed for heating purposes is in the Northeast and Midwest of the country.</p>
<p>



The U.S. Energy Information Administration strives to create value by providing timely and accurate data to the public. If you are interested in having your State added to this survey, please contact the Survey Manager, Marcela Rourk at <a href="mailto:marcela.rourk@eia.gov">marcela.rourk@eia.gov</a>. Your input will be taken into consideration in determining the future of the survey.</p>
<p>



In addition, you may want to check to see if your State Energy Office collects these data independent of this survey.</p>
<h3>Q7: Why were 14 states added to the survey in 2014?</h3>
<p>



For the first time since 1994, States were added to the survey in response to the propane supply shortage and sharp increases in propane prices throughout the United States during the 2013-2014 winter heating season. State Energy Offices in all states have been invited to participate in residential propane price data collection as part of the State Heating Oil and Propane Program.</p>
<h3>Q8: Why are wholesale prices not reported in some states where residential prices are reported?</h3>
<p>



EIA publishes wholesale prices for heating oil and propane calculated from the Oil Price Information Service (OPIS). OPIS collects such data at the city level, and some states do not have a sufficient number of cities within the OPIS survey to calculate a statewide average.</p>
<h3>Q9: Why is the price for last week slightly different than the price I saw last week. Does this mean that the data are different for the whole data series?</h3>
<p>



Prior week residential heating oil and propane prices may be revised during the week following publication. Revisions may be made when a price discrepancy for an outlet in the survey is discovered after the publication timeline is closed.  Unless otherwise noted, prices will only be revised one week following publication.</p>

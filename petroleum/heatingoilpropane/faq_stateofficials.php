</p>
<h3>Q8:  How does my State Energy Office draw grant funds?</h3>
<p>The U.S. Department of Energy has transitioned to an electronic procurement system. As a result, the Department is currently utilizing <a href="https://www.fedconnect.net/fedconnect/">FedConnect</a> as the sole avenue for monetary disbursements. Therefore it is vital that all recipients register with FedConnect at their earliest convenience. As in the past, please note that the distribution of money will be made in one lump sum as opposed to periodic disbursements.
</p>
<p>To facilitate receipt of payment, please also register as a participant in the <a href="https://www.asap.gov/">Automated Standard Application for Payments</a> (ASAP) program if you have not done so already. This program is set up by the U.S. Department of the Treasury to allow States receiving federal funds to draw from accounts pre-authorized by Federal agencies, including the U.S. Department of Energy. Please visit the <a href="https://www.asap.gov/">ASAP website</a> for more information.
</p>
<h3>Q9:  Will I be notified when Grants are awarded?</h3>
<p>After submitting the Grant applications, States should routinely check <a href="https://www.fedconnect.net/fedconnect/">FedConnect</a> to determine when their award funds are ready for disbursement.  In addition, the States will receive an email communication informing them that their grant funds are available.  To get updates on the status of grant availability, please contact Marcela Rourk at <a href="mailto:marcela.rourk@eia.gov">marcela.rourk@eia.gov</a>.
</p>
<h3>Q10:  There has been reorganization within our office, and SHOPP will now be the responsibility of another agency within the State. What do I need to do to ensure that the new agency receives SHOPP funding?</h3>
<p>As soon as you become aware of any reorganization within your State that will impact SHOPP, contact Marcela Rourk at <a href="mailto:marcela.rourk@eia.gov">marcela.rourk@eia.gov</a>.  The process of transferring the SHOPP award from one agency to another can be time-consuming, and it is imperative to begin making changes within the system as soon as possible.  You will need to provide EIA with
</p>
<ol>
    <li>New agency name</li>
    <li>Address</li>
    <li>New point of contact information</li>
    <li>Telephone number</li>
    <li>Email</li>
</ol>
<p>
In addition, the new agency will need a <strong>DUNS number</strong>, and will need to make sure they are registered correctly on the <a href="https://www.sam.gov/portal/SAM/">System for Award Management (SAM)</a> website. The information on the new agency provided to EIA should match the information on SAM.
</p>
<p>The new agency will also need to register with <a href="https://www.fedconnect.net/fedconnect/">FedConnect</a> and with the <a href="https://www.asap.gov/">Automated Standard Application for Payments</a> (ASAP) program to ensure that they can receive their annual SHOPP disbursements.
</p>
<p>If this change occurs during the SHOPP season, States are encouraged to transfer funds for that annual cycle between State agencies, as making changes can delay access to SHOPP grant funds for the new agency. In either case, the SHOPP Survey Manager, Marcela Rourk, should be made aware of the situation as soon as possible.
</p>

<h2>FAQs for State Energy Offices</h2>
<h3>Q1:  What price should be reported to EIA when submitting weekly data?</h3>
<p>EIA requests that you collect / report the <strong>residential credit price</strong> (keep-full prices being preferred) and that all prices exclude taxes for the Monday of each survey week, even if that Monday falls on a holiday. Prices should not include discounts for payment of cash or for payment made within a short period of time. However, if a company deals exclusively in cash, then this price should be reported and noted in the file sent to EIA.
</p>
<h3>Q2:  When is this data due to EIA each week?</h3>
<p>The EIA-877 "Winter Heating Fuels Telephone Survey" will begin the first Monday in October. Data should be submitted to EIA as soon as they are available <strong>but no later than noon on Tuesday</strong> of each week. Data collection will continue each Monday on a weekly basis through March.
</p>
<h3>Q3: How does SHOPP/EIA-877 operate over holidays? </h3>
<p>The data collection schedule is generally pushed back one day in the week if the holiday falls on a Monday or Tuesday. If the holiday falls on a Wednesday, Thursday, or Friday, then there is no change to the reporting schedule. Instructions pertaining to holiday schedules will be updated in the notices section within the IDC.
</p>
<h3>Q4: I plan to be out of the office, how should I handle data collection?</h3>
<p>Your office is still responsible for data collection even if you are out of the office.  You should designate a back-up person for data collection in the event that you are sick or on vacation.  To facilitate with training of back-up personnel EIA has recorded a basic <a href="/petroleum/heatingoilpropane/training/video/datacollection.mp4">training</a> session on data collection.
</p>
<p>If your office is closed or you run into another issue that will prevent your office from collecting and/or reporting data on time, please contact Marcela Rourk at (202) 586-4412 or <a href="mailto:marcela.rourk@eia.gov">marcela.rourk@eia.gov</a> as soon as you are aware of the potential problem so that we can make other arrangements.
</p>
<h3>Q5: In the event of furlough days, how does my office proceed with handling their EIA-877 survey responsibilities?</h3>
<p>In the event that furlough days fall within the week's data collection cycles, please contact the EIA-877 Survey Manager, Marcela Rourk: <a href="mailto:marcela.rourk@eia.gov">marcela.rourk@eia.gov</a>, (202) 586-4412.
</p>
<h3>Q6:  How do I enter prices into the Internet Data Collection (IDC) system?</h3>
<p>First, please verify that you can login to the <a href="https://signon.eia.doe.gov/ssoserver/login">IDC</a> before the start of the season. The instructions for the IDC are available once you have logged onto the server. There is a link from the "Instructions & Help" section to a printable version.
</p>
<h3>Q7: How does my State participate in the EIA-877?</h3>
<p>Data is collected from States which have been determined to consume a significant amount of heating oil and/or propane for residential heating purposes during the winter months. EIA receives a fixed amount of funding each year from Congress to fund the EIA-877, and periodically reviews the possibility of including new States in the survey. States that choose to participate are asked to enter into a five-year cooperative agreement with the U.S. Energy Information Administration. As part of the cooperative agreement, States collect data on heating oil and propane prices on a weekly basis during the winter heating season based on a sample provided to them by EIA. These data are then aggregated and published weekly on the EIA website. These efforts are funded jointly by State and Federal funds, with each accounting for 50% of the program cost.
</p>
<p>Our five year project period started in August 2011. It is projected that the next five year cooperative agreement project period will start around August 2016. If your State is interested in participating in the EIA-877, please contact Marcela Rourk at <a href="mailto:marcela.rourk@eia.gov">marcela.rourk@eia.gov</a> or (202) 586-4412.
</p>

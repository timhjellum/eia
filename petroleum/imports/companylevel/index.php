<p>
<h3>June 2017 Import Highlights</h3>
Monthly data on the origins of crude oil imports in June 2017 show that two countries, Canada and Saudi Arabia, exported more than one million barrels per day (b/d) to the United States (see table below). The top five exporting countries accounted for 77% of United States crude oil imports in June while the top ten sources accounted for approximately 91% of all U.S. crude oil imports. The top five sources of U.S. crude oil imports for June were Canada (3.3 million b/d), Saudi Arabia (1.0 million b/d), Mexico (676,000 b/d), Venezuela (616,000 b/d), and Iraq (570,000 b/d). The remaining top ten sources, in order, were Colombia (313,000 b/d), Nigeria (284,000 b/d), Ecuador (212,000 b/d), Angola (178,000 b/d), and Algeria (165,000 b/d). Total crude oil imports averaged 8.0 million b/d in June, which was a decrease of 387,000 b/d from imports during May 2017.
</br></br>
Canada remained the largest exporter of total petroleum to the United States in June; exporting 3.8 million b/d. The second largest exporter of total petroleum was Saudi Arabia with 1.0 million b/d.

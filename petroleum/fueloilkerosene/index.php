<p>
The <em>Fuel Oil and Kerosene Sales 2015</em> report provides information, illustrations, and State-level statistical data on end-use sales of kerosene; No.1, No. 2, and No. 4 distillate fuel oil; and residual fuel oil. State-level kerosene sales include volumes for residential, commercial, industrial, farm, and all other uses. State-level distillate sales include volumes for residential, commercial, industrial, oil company, railroad, vessel bunkering, military, electric utility, farm, on-highway, off-highway construction, and other uses. State-level residual fuel sales include volumes for commercial, industrial, oil company, vessel bunkering, military, electric utility, and other uses.
</p>
<p>
The Office of Petroleum and Biofuels Statistics ensures the accuracy, quality, and confidentiality of the published data in the <em>Fuel Oil and Kerosene Sales 2015</em>.
</p>
<p>
NOTE: The 2014 adjusted numbers have been revised since they were first published in the <em>Fuel Oil and Kerosene Sales 2014</em>. The revisions to the 2014 data were made to include updated Products Supplied volumes and revised distillate and residual fuel oil volumes for electric power. See Electric Power of Technical Note 3 in Technical Notes of the <em>Fuel Oil and Kerosene Sales 2015</em> for further explanation. We have included an <a href="/petroleum/fueloilkerosene/xls/eia_821_data_difference.xls">excel file</a> that shows the differences between the original and revised published data for your convenience.
</p>

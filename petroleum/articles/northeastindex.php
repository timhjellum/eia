<h1>Impact of Interruptible Natural Gas Service on Northeast Heating Oil Demand</h1>
<p class="date">February 1, 2001<br />
</p>
<p><em>Impact of Interruptible Natural Gas Service on
Northeast Heating Oil Demand</em> was undertaken at the
request of U.S. Secretary of Energy Bill Richardson to
assess the extent of interruptible natural gas contracts and
their effect on heating oil demand in the Northeast. An
earlier report with policy recommendations was issued by
the Department of Energy&#8482;s Office of Policy in
November 2000 that examined the effect of interruptible
contracts in New England. The current report expands
the geographic scope of the analysis by including New
Jersey, New York, and Pennsylvania and presents a more
comprehensive assessment of gas service interruptions,
the responses of different types of customers, and the
effects on the distillate fuel oil market.
</p>
<p>The report is based on the results of two surveys
developed by the Energy Information Administration
(EIA): Form EIA-903, "Natural Gas Service Interruptions
in the Northeast During December 1999, and January and
February 2000,"? and Form EIA-904, "Customer Survey
of Natural Gas Service Interruptions in the Northeast
During January and February 2000."? The respondents to
Form EIA-903 were 34 natural gas companies who
provided 94 percent of natural gas deliveries to
interruptible gas customers in the Northeast in 1998,
while respondents to Form EIA-904 were 97 end users in
New England who were identified by their suppliers as
experiencing natural gas interruptions in the winter of
1999-2000.
</p>
<p>The report has five chapters and four appendices.
Chapter 1 gives an overview of the Northeast heating oil
and natural gas markets during the winter of 1999-2000.
Chapter 2 provides background information on natural
gas markets in the Northeast and the role of interruptible
contracts in the region&#8482;s energy market. Chapter 3
examines the main factors that affect heating oil and
natural gas prices by comparing market events during other periods of sharp price increases in recent years.
Chapter 4 provides an analysis of the information derived
from the EIA surveys of gas suppliers and customers, and
Chapter 5 summarizes the market implications.
</p>

<h1>Inquiry into August 2003 Gasoline Price Spike</h1>
<p class="date">December 1, 2003<br />
</p>
<p>In July and August of 2003, the U.S. average retail price for regular gasoline rose 26
cents per gallon in 8 weeks, to an all-time record high (not adjusted for inflation) of $1.75
per gallon. Twelve cents of this increase occurred in one week, between
August 18 and 25, making it the largest one-week increase in gasoline prices recorded by
the Energy Information Administration (EIA). Out of concern over the size and speed of
this price rise, and the background against which it occurred â€“ a power blackout in the
Midwest and Northeast, a pipeline rupture in Arizona, and a variety of other supply and
demand issues â€“ U.S. Secretary of Energy Spencer Abraham requested that EIA conduct
an inquiry into the causes of the price increases.
</p>
<p>The report considers the relationships among prices at three distinct market levels: crude
oil, wholesale (spot) gasoline, and retail gasoline. In contrast to some past
nationwide gasoline price spikes, crude oil prices changed little in August and, thus, did
not add to the late-summer gasoline price increases. Moreover, the relationship between
wholesale and retail prices was broadly consistent with past experience. Therefore, the report focuses on factors contributing to changes in prices at the wholesale level, which
actually increased more sharply than retail gasoline prices from late July to late August.
</p>
<p>Pursuant to the Secretary&#8482;s request, this study considers the role of inventory levels for
gasoline and crude oil prior to and during the summer, other supply issues such as
refinery and pipeline outages, and demand factors. Our main findings show that all of
these areas contributed significantly to the August gasoline price spike.
</p>
<ul>
<li>Inventories, which are needed to help balance unexpected changes in supply or
demand, began the summer driving season at low levels and did not recover
A major strike in Venezuela, the Iraq war, and unrest in Nigeria all
removed crude oil from world markets in late 2002 and early 2003, creating a
need for the United States to draw down both crude oil and product inventories at
that time.
</li><li>Demand growth was generally anemic early in the summer, which moderated
prices despite low inventories and problems with refineries and pipelines that
precluded a rebuilding of gasoline stocks. However, gasoline demand grew
rapidly in July and August.
</li><li>Supply factors played a role in the regional price spikes in August. After
beginning the spring season with low inventories, refiners ran at very high
utilization levels in April and May in most regions with some stock rebuilding. In
June and July, refinery outages occurred in the Midwest, Gulf Coast, and West
Coast. The reduction in gasoline production from the outages ended the stock
build. Gulf Coast gasoline production recovered in August, but the upturn was
too late to meet rising demand and prevent rapid stock declines in the East Coast
and Midwest. The Midwest lost additional gasoline production in August because
of the August 14 electricity blackout. On the West Coast, California production
in August had recovered from earlier outages, but was unable to keep up with the
added demand from Arizona due to a pipeline rupture.
</li><li>In combination, August gasoline demand exceeded supply coming from U.S.
refineries and from imports, resulting in a rapid decline in already-low
inventories. This resulted in gasoline price surges throughout the country.
Because supplies in the Midwest, East Coast, and Gulf Coast are linked, the price
increases east of the Rocky Mountains were related. The California price rise was
not directly linked to the other regions, but lack of supply from Gulf Coast
refineries increased Californiaâ€˜s difficulty in acquiring additional volumes.
</li>
</ul>

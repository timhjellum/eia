<h1>Petroleum 1996: Issues and Trends </h1>
<p class="date">September 1, 1997<br />
</p>
<p><strong>Purpose of the Report
</strong></p>
<p>Increasingly, users of the Energy Information Administration's
petroleum data and analytical reports have expressed
an interest in a recurring report that takes a broad view of the
petroleum sector. What is sought is some perspective on the
complex interrelationships that comprise an industry and
markets accounting for 40 percent of the energy consumed
in the United States and ranging from the drilling rig in the
oil field to the pump at the local gasoline station.
</p>
<p>This report comprehensively examines historical <em>trends</em>, and
selectively focuses on major <em>issues</em> and the events they
represent. It analyzes different dimensions of the industry
and related markets in terms of how they relate to a common
theme, in this case, the volatility in petroleum markets.
</p>
<p><strong>Theme â€” Volatility
</strong></p>
<p>One notable development in petroleum product markets has been volatility, as reflected in the rapid and conspicuous change in petroleum product prices. An instance of volatility, the turbulence in motor gasoline wholesale and retailmarkets in the spring of 1996, made this a matter of national concern. At the time, the Energy Information Administration
co-authored a report undertaken at the request of President
Clinton, which examined the market conditions that
impacted the supply-demand balance leading up to the motor
gasoline season, and how the wholesale, commodity, and
retail markets responded. In a sense, this report, <em>Petroleum
1996: Issues and Trends</em>, picks up where that report left off.
</p>
<p>More recently, the issue of volatility in petroleum markets
was addressed by Secretary of Energy Federico PeÃ±a in a
message to Department of Energy employees throughout the
country on the day after he was sworn into office. In
outlining the first of four priorities for the Department,
Secretary PeÃ±a underscored a major long-term source of
uncertainty in the world supply/demand balance:<br />
<br />
"First, we will develop a realistic strategy for
strengthening our nation&#8482;s energy security. Recent history has demonstrated how vulnerable we are to a disruption
in the flow of imported oil or volatility in its price."
</p>
<p>The volatility, the sharp and rapid fluctuation, in oil prices
is a symptom of a combination of underlying market
developments. Sharp price movements reflect a high level of
uncertainty in all sectors of supply and their associated
markets. Thus, more insight may be gleaned from assessment
of the supply and demand conditions that led to the shift in
prices, in addition to analyzing the impacts on participants in
the petroleum markets--suppliers, consumers, investors.
</p>
<p>Over the past few decades, the petroleum industry in the
United States has undergone some fundamental changes in
its operations. These changes range from declines in
domestic oil production, and in crude and product inventory
levels, to emerging new technologies; from new
environmental requirements that affect the costs of its
operations, to new financial markets that affect the prices of
its products. When such changes evolve gradually, they can
be absorbed smoothly in the markets. But when changes in  supply and demand occur suddenly or unexpectedly, they can lead to sharp market reactions. The resulting rapid price  changes are symptoms of these reactions â€” evidence of a market imbalance, and the signal that a market correction is underway.
</p>

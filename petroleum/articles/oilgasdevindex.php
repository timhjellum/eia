<h1>Oil and Gas Development in the United States in the Early 1990's </h1>
<p class="date">October 15, 1995<br />
</p>
<p>Since the oil price collapse of late 1985 and early 1986,
the U.S. oil and gas industry has changed dramatically.
The major oil companies have shifted much of their exploration
and development efforts to targets outside the
United States. Although hundreds of nonmajor oil and
gas producers were casualties of the price collapse, the
survivors became more important players in the industry.
Investment by the nonmajors in U.S. oil and gas
resource development increased from about 33 percent
of total U.S. exploration and development expenditures
in 1988-1990 to nearly 50 percent in recent years.
</p>
<p>The increasing dependence of U.S. oil and gas
development on the typically much smaller nonmajor
companies raises a number of issues, which are
analyzed in this report. Did those companies gain
prominence by default, or have they been significantly
adding to the U.S. reserve base? What are the
characteristics of surviving producers compared with
companies that exited the industry? Do the nonmajors
add reserves at higher cost than the majors, or possibly
pay more for borrowed funds?
</p>
<p>To identify areas of growth of nonmajor oil and gas
producers in the context of the entire U.S. industry, this
analysis first distinguishes companies as major versus
nonmajor petroleum companies. The analysis draws
heavily on data from the Energy Information Administration&#8482;s
(EIA) Financial Reporting System, as well as
relying on data from EIA&#8482;s oil and gas reserves report
series.
</p>
<p>In order to further analyze the corporate features of the
nonmajors, the companies are further classified by
characteristics such as degree of specialization and
corporate structure. In particular, the companies
specializing in oil and gas production, generally termed
the "independents,"? were the ones mainly responsible
for the nonmajors&#8482; growth in oil and gas production.
This report uses publicly available company data to
evaluate financial results and arrive at conclusions
about the independents&#8482; standing in the capital markets.
</p>
<p>The oil price collapse provides a backdrop to assess the
effectiveness of corporate strategy and the influence of
investor evaluations on independent oil and gas producers.
The analysis highlights the differences in resource development and financial results for firms
that survived the oil price collapse and firms that did
not survive. Comparison to the majors offers an
additional benchmark of financial and operating performance
</p>
<p>Chief findings of this report include:
<ul>
<li><em>Offshore Gas Led Upswing in Nonmajors&#8482; Production
Post-1986</em>. The nonmajors&#8482; production of natural
gas from offshore locales more than doubled in
absolute terms, from 741 billion cubic feet in 1986
to 1,526 billion cubic feet in 1993. This mainly
reflected reserve additions, but stepped-up
depletion rates also contributed to increased
production.
</li>
<li><em>Onshore Oil Focus of Growth for Nonmajors</em>. The
nonmajors&#8482; share of U.S. oil production from the
onshore lower 48 increased from 45 percent (835
million barrels) in 1989 to 54 percent (935 million
barrels) in 1993, due to increased production and
exit of the majors from these areas.
</li>
<li><em>Surviving Independents&#8482; Reserve Replacement Costs
are Now Similar to Majors.</em> The cost of reserve
replacement (including purchases) for the surviving
independents decreased over the 1986-1993
period, and by the 1990&#8482;s was almost equal to that
of the majors at about $5 per barrel. However, the
overall cost of exploring for and developing oil
and gas reserves (which does not include purchases)
was higher for the surviving independents
than for the majors, even into the 1990&#8482;s.
</li>
<li><em>Capital Markets Primarily Value Low Cost Reserve
Replacement.</em> The publicly traded independent
producers that survived through 1993 tended to
incur lower costs in replacing reserves than those
companies that exited the U.S. oil and gas industry.
And, in spite of greater reliance on debt
and lower returns than the majors, the publicly
traded independents&#8482; cost of capital is comparable
to the majors&#8482;. This finding indicates that the
market has tended to value low cost resource
development more than conventional measures of
financial performance
</ul>
</p>

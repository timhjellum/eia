<p>The currently reported remaining oil reserves for the Persian
Gulf countries of Iran, Iraq, Kuwait, Qatar, Saudi Arabia, Abu
Dhabi, and Dubai are about 585 billion barrels (World Oil,
August 1994). Current production is about 19 million barrels
per day (assuming Iraqi production at pre-embargo rates).
This yields an annual reserves-to-production (R/P) ratio of
about 83. A country may more prudently develop its oil
reserves at an R/P ratio near 20. Developing reserves that will
not be produced for many years is economically unwise.
Applying an annual R/P ratio of 20 to the current production
rate yields an estimate of developed reserves of about 140
billion barrels. This leaves about 445 billion barrels of remaining undeveloped (or non-producing) oil reserves for the Persian Gulf area. (In addition, the U.S. Geological Survey estimates undiscovered resources of about 112 billion barrels for these same countries.)
</p>
<p>The Energy Information Administration (EIA) forecasts oilproduction capacity from Iran, Iraq, Kuwait, Qatar, Saudi Arabia, and the United Arab Emirates to grow to from about 20 million barrels per day currently to 30.0 to 36.6 million barrels per day in the year 2010 (<em>International EnergyOutlook 1995</em>). Applying the mid-case values from the
summary table above to the projected production capacity
requires additional developed reserves of 54 to 83 billion
barrels at a cost of 34 to 53 billion dollars by the year 2010.
This assumes current capacity can be maintained without
replacing produced reserves from an equal quantity of
undeveloped reserves. The undeveloped reserves determined
above (445 billion barrels) are more than adequate to support
the projected demand. This scenario does not include the
replacement of produced reserves by the development of
undeveloped reserves. Currently, the replacement of produced
reserves is minor, but will grow to significant levels in the
future (exact timing is unknown). (Appendix A contains
tables and graphs that address the replacement of produced
reserves using the same costs as used for adding capacity. If
the replacement of production is included, the comparable
volume of undeveloped reserves to be developed is 194 to 248
billion barrels at a cost of 127 to 161 billion dollars through
2010 (mid-case). The actual costs will likely be somewhere
between those associated with no replacement of produced
reserves and those associated with total replacement of
produced reserves.) </p>

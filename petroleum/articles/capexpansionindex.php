<h1>Oil Production Capacity Expansion Costs for the Persian Gulf</h1>
<p class="date">January 1, 1996<br />
</p>
<p>Countries surrounding the Persian Gulf supply 27 percent of
world oil demand and 22 percent of U.S. imports. The Energy
Information Administration&#8482;s <em>International Energy Outlook</em> projects that, by the year 2010, these countries will be asked
to supply 39 percent of the world&#8482;s thirst for oil. U.S. imports
are expected to grow 48 percent over the same time period.
Persian Gulf oil supplies will increase in importance to both
the global and U.S. oil markets. Persian Gulf countries will
need to invest large sums to increase production capacity to
meet the demand.
</p>
<p>Persian Gulf oil development, production, and operating costs
have received the attention of industry analysts in recent
years . Concerns range from the revenue available for the
local economy, to the investment necessary to meet the
expected growth in oil demand. The total cost of the projected
capacity expansion is a relatively small percentage of the gross
revenue. However, oil export revenues provide the vast
majority of government income in the Persian Gulf countries.
Typically, national oil companies have to compete with
defense spending, social programs, and other government
services for investment funds from their governments. Other
options are for the government/company to borrow the
investment funds or allow foreign companies to acquire equity
positions. The advantages and disadvantages of these funding
options vary among the Persian Gulf countries. Before the
year 2010, the Persian Gulf countries will have to develop
between 38 and 251 billion barrels of oil at a cost of 31 to 371
billion dollars to meet the expected demand for Persian Gulf
oil.
</p>
<p>This report attempts to quantify the cost of expanding oil
production capacity using individual geologic plays in the
countries surrounding the Persian Gulf through the year 2010.
The countries included in this report are Iran, Iraq, Kuwait,
Qatar, Saudi Arabia, Abu Dhabi, and Dubai
</p>
<p>Petroconsultants' <em>Estimator</em> database and software were used
to define eight geologic plays in formations of Mesozoic and
Tertiary ages, both onshore and offshore. (Data for Saudi
Arabia and Kuwait are not available in this database, but the
surrounding plays are assumed to be representative for these
countries.) Development and operating cost scenarios based
on the three field sizes provided by Estimator for each play
were defined yielding low-, mid-, and high-case values.
Because this analysis is focused on oil, all gas production is
assumed to be flared so that all costs are for oil production
only. (In practice, the flaring of gas has been curtailed and
normally is done only at remote locations.) All costs are in
constant 1994 dollars (no inflation or discounting was used).
</p>
<p>Costs for the eight plays and three field sizes were calculated
and averaged for each of the three cases (low, mid, and high
field sizes) to yield low-, mid-, and high-case cost estimates
for the Persian Gulf area. The following summary table
defines the investment cost in two ways: 1) the cost of
increasing production capacity, 2) the cost of developing
reserves. Also shown are the production operating expense
and a peak daily production yield per developed reserves.
</p>

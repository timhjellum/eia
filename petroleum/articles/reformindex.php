<h1>The Reformulated Gasoline Market Affected Refiners Differently</h1>
<p class="date">anuary 1, 1996<br />
</p>
<p><strong>Introduction</strong> </p>
<p>The United States has completed its first summer
driving season using reformulated gasoline (RFG).
Motorists noticed price increases at the retail level,
resulting from the increased cost to produce and
deliver the product, as well as from the tight supply/demand
balance during the summer. This article
focuses on the costs of producing RFG as
experienced by different types of refiners and on how
these refiners fared this past summer, given the prices
for RFG at the refinery gate.
</p>
<p><strong>RFG Regulatory Requirements
</strong></p>
<p>The use of RFG is a result of the Clean Air Act
Amendments of 1990 (CAAA). The CAAA cover a
wide range of programs aimed at improving air quality,
including a program to reduce emissions from
automobiles, through the use of (a) oxygenated fuels
targeting carbon monoxide emissions and (b) RFG
targeting ozone and toxic pollutants. Use of RFG was
required in 9 areas rated as being in severe or extreme
non-attainment. Other less severe areas were allowed
to choose to be in (opt into) the program. The
major areas using RFG this past year were in the
Northeast, the Midwest (around Chicago and Milwaukee),
and Southern California.
</p>
<p>The RFG program is being implemented in two
phases, each of which contains both prescriptive and
performance standards. Industry preferred performance
standards, where possible, to allow some flexibility
in how companies could meet the standard.
The two phases target different levels of emissions
reduction.
</p>
<p>As shown in Figure FE1, Phase I targets reducing
hydrocarbon emissions by 15 percent during the
high ozone season (summer), and reducing toxic
emissions by 15 percent, or requiring fuel performance
to meet the standards of a formula fuel, whichever
produces the greatest reduction. In addition,
Phase I requires that nitrogen oxides (NOx) not increase.
Phase II has a more aggressive goal than
Phase I, targeting 20 to 25 percent volatile organic
compound (VOC) and toxic emission reductions.
The non-performance or prescriptive requirements
under both Phase I and II are shown in Figure FE2.
</p>
<p>Phase I began in December of 1994 at the wholesale
level of distribution, and was in place by January 1995
at the retail level. Within Phase I, there are two means
of compliance: the simple model and the complex
model. Currently, refiners are complying under
what is called the "simple model,"? which EPA established
at the end of 1991 to meet the requirements of
the legislation. This "simple model"? formed the
guidelines for refiners from data available at that time
relating fuel parameters (oxygen content, Reid vapor
pressure (RVP), benzene, aromatics) to emissions.
</p>
<p>Other parameters such as sulfur, T90,3
and olefins
were capped at a refiner&#8482;s 1990 baseline level to keep
from having these parameters undercut achievements
in emissions reduction from parameters being
controlled in the simple model. The simple model
can be used by refiners until January 1998, when the
"complex model"? must then be used to meet Phase I
standards. (EPA issued the complex model performance
standard early in 1994.)
</p>
<p>Phase II begins in the year 2000. During Phase II,
refiners will certify RFG according to the Phase II
standards using the complex model.
</p>
<p>This article concentrates on the economics of complying
with the simple model under Phase I. Table FE1
shows the RFG compliance strategies under the simple
model. Refiners were given a choice of meeting
requirements on an individual barrel basis, or on an
average basis. As shown in Table FE1, to comply
with the VOC control requirements of the CAAA,
oxygen content and RVP are controlled. The simple
model formula for toxics emissions is a function of
fuel benzene, fuel aromatics, RVP, type of oxygenate,
and oxygen content.
</p>
<p>In addition, the regulations stipulate that for conventional
gasoline:
<ul>
<li>benzene content cannot exceed the refinery&#8482;s 1990
baseline level;
</li>
<li>sulfur, T90 and olefins must be capped at 1.25
times a refinery&#8482;s 1990 level.
</ul>
</p>
<p>The intention of these requirements is to prevent
"dumping"? undesirable products, such as benzene
and aromatics that will be removed from RFG, into
conventional gasoline.
</p>

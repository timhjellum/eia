<p>As the winter season approaches, distillate markets this year
provide a series of stark contrasts to distillate markets last year. A year ago, distillate markets were on a precipice.
Record-low stocks created fears of possible supply disruptions and
soaring prices for the core winter months. These fears were not
realized, however, as warm weather, record-high refinery output
and smooth logistics, kept winter distillate markets calm. Last
year&#8482;s autumn stock levels did not represent a fundamental change
in distillate markets. The factors that led last year to such low stock
levels have not been repeated this year. Hence, stocks are higher
and markets less vulnerable (but not invulnerable) to price spikes.

<h1>Propane Market Assessment for Winter </h1>
<p class="date">December 1, 1997<br />
</p>
<p>This article reviews the major components of propane supply
and demand in the United States and their status entering the
1997-1998 heating season. Other influences on prices are also
discussed. Finally, a base case and two alternative scenarios
are described for the heating season assessment. The alternative
scenarios focus on inventory levels and residential prices.
</p>
<p>Propane markets entering the 1997-1998 heating season face
an environment in sharp contrast to what confronted propane markets a year earlier. In late summer 1996, in response to
relatively low inventory levels and tight world oil markets,
prices for crude oil, natural gas, and products derived from
both, including propane, began to increase rapidly ahead of the
winter heating season. Both public and private consensus
foresaw the potential for supply shortfalls and continued sharp
price increases, especially in the event of unusually severe
winter weather. In particular, propane markets were subject to
a combination of factors that caused extreme market volatility,
resulting in spot propane prices reaching 50 cents per gallon by
the start of the heating season, and rising to 107.5 cents per gallon by mid-December. Some of these factors were as
follows: rising crude oil prices; colder-than-normal weather
early in the season; heavy crop drying demand in the Midwest;
and the mid-summer explosion at a Mexican gas plant that
essentially halted that country&#8482;s propane exports to the U.S.
</p>
<p>The outlook for propane supply and prices during the
1997-1998 heating season appears to be significantly more
favorable for consumers than that of the past winter. The
factors that caused propane prices to spike last year are not
expected to be repeated this year. Several of the major factors
that drive this assessment include lower crude oil prices, higher
U.S. and regional inventories, and a return to seasonal demand
levels for both heating and for crop drying applications.
</p>
<p>As of September 30, 1997 (beginning of the heating season),
U.S. inventories of propane totaled 60.2 million barrels, 8.3
million barrels above the year earlier level and the highest
pre-heating season since 1994. Furthermore, this
level remains above the normal range for this time of year.
Regionally, inventories were within or above their normal
ranges for all the key propane supply/demand areas of the
Nation. East Coast inventories stood at 4.6 million barrels as
of September 30, 1997, a level slightly below the previous
year&#8482;s level but within the normal range for this month.
Midwest inventories measured 27.5 million barrels as of the
start of the heating season, a level well above the normal range
for this month and 4.8 million barrels above the year ago level.
Gulf Coast inventories began the heating season at about 26
million barrels, 4.4 million barrels above the same period last
year and well within the normal range for a pre-heating season
period.
</p>
<p>Evaluating the accuracy of last year&#8482;s projection of propane
supplies, actual weather conditions during the 1996-1997
winter heating season more closely resembled the "Base Case"?
scenario than the alternate scenarios that called for more adverse temperatures. Temperatures (as measured by heating
degree days) last winter averaged about 4 percent above
normal throughout the United States. The "Base Case"?
scenario assumed normal winter temperatures and
non-weather-related demand and supply factors remaining at
typical historical levels, with primary stocks ending the heating
season at approximately 21 million barrels. Although cold
temperatures early in the heating season contributed to pull
stocks down more sharply than the projected path in the "Base
Case"? scenario, milder temperatures beginning in January
helped to push stocks above the expected path for the remainder
of the heating season. By the end of March, stocks stood at
27.6 million barrels, 6.6 million barrels above the "Base Case"?
projection.
</p>

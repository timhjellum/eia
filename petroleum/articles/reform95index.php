<h1>Demand, Supply, and Price Outlook for Reformulated Motor Gasoline 1995</h1>
<p class="date">July 1, 1994<br />
</p>
<p>Provisions of the Clean Air Act Amendments of
1990 designed to reduce ground-level ozone will
increase the demand for reformulated motor gasoline
in a number of U.S. metropolitan areas. Reformulated
motor gasoline is expected to constitute
about one-third of total motor gasoline demand in
1995, and refiners will have to change plant operations
and modify equipment in order to meet the
higher demand. The costs incurred are expected
to create a wholesale price premium for reformulated
motor gasoline of up to 4.0 cents per gallon
over the price of conventional motor gasoline. This
article discusses the effects of the new regulations
on the motor gasoline market and the refining
industry.
</p>
<p>The reformulated motor gasoline provisions of the Clean Air
Act Amendments of 1990 (CAAA90) require reductions in
automobile emissions of ozone-forming volatile organic
compounds (VOC) during the summer high-ozone season
and of toxic air pollutants (TAP) during the entire year in
certain areas of the United States. The new regulations,
which go into effect December 1, 1994, mandate the sale of
reformulated motor gasoline in the nine largest metropolitan
areas with the highest summer ozone levels and other ozone
nonattainment areas that opt in to the program. (Some nonattainment
areas with less severe ozone problems will pursue
other measures to achieve the required ozone
reductions; therefore, there will be no need to sell reformulated
motor gasoline in those areas.) The regulations also
prohibit motor gasoline sold in the rest of the country from
becoming more polluting than it was in 1990. That provision
is intended to ensure that refiners do not use ingredients
in conventional motor gasoline that can no longer be
used in reformulated motor gasoline.
</p>
<p>This article both analyzes the new regulations&#8482; impact on the
motor gasoline market and evaluates the constraints and
costs faced by the petroleum refining industry in complying
with the new regulations. The forecasts in this article are
based on forecasts in the Short-Term Energy Outlook, which is published quarterly by the Energy Information Administration. The supply, demand, and price forecasts in this article do not include provisions for a required minimum use of renewable oxygenates, which had been proposed but not yet promulgated by the Environmental Protection Agency (EPA) at the time this article was prepared.
</p>
<p>Demand for reformulated motor gasoline is expected to
represent almost 35 percent of total motor gasoline demand
in 1995. Demand projections for reformulated motor gasoline
are based on the 1990 populations of the participating
ozone nonattainment areas and projected per capita motor
gasoline demand in each area. Corrections are made for
spillover of reformulated motor gasoline to areas that will
not legally require it, changes in automobile fuel efficiency,
and price elasticity of demand.
</p>
<p>Refineries will have to change operating procedures, make
plant modifications, and obtain new process equipment in order
to meet the new oxygenate, vapor-pressure, and benzene specifications
and the emissions reduction requirements for reformulated
motor gasoline. However, significant
disruptions to motor gasoline supply arising from the reformulated
motor gasoline regulations are not anticipated.
</p>

<h1>Motor Gasoline Assessment, Spring 1997</h1>
<p class="date">September 1, 1997<br />
</p>
<p>The springs of 1996 and 1997 provide an excellent example of contrasting gasoline market
dynamics. In spring 1996, tightening crude oil markets pushed up gasoline prices sharply, adding to
the normal seasonal gasoline price increases; however, in spring 1997, crude oil markets loosened
and crude oil prices fell, bringing gasoline prices down. This pattern was followed throughout the
country except in California. As a result of its unique reformulated gasoline, California prices
began to vary significantly from the rest of the country in 1996 and continued to exhibit distinct
variations in 1997. In addition to the price contrasts between 1996 and 1997, changes occurred in
the way in which gasoline markets were supplied. Low stocks, high refinery utilizations, and high
imports persisted through 1996 into summer 1997, but these factors seem to have had little impact
on gasoline price spreads relative to average spread.
</p>
<p>Chapter 2 summarizes and contrasts the events of the two springs, illustrating the interactions
between U.S. gasoline markets, which did not change appreciably, and crude oil markets, which
went from tight to loose.
</p>
<p>Chapter 3 takes a deeper look at what has recently been changing in gasoline supply, focusing
particularly on imports and the yield changes seen over the past several years.
</p>
<p>Chapter 4 focuses on California, which is becoming an almost isolated market. In 1996, the
uncertainties surrounding the ability to supply its new, unique, reformulated gasoline (CaRFG)
pushed prices to record highs as refineries stumbled getting the program underway. In a dramatic
turnaround, prices plummeted in the fall as oversupply caused dumping of gasoline and a price war
in southern California. As 1997 unfolded, new refinery problems again brought supply concerns
that increased prices.
</p>
<p>Chapter 5 looks across regions to explore gasoline price behavior by class of trade. Resale price
spreads over crude oil cost are also inspected to see if there is any major difference between the
patterns of 1996 and 1997 from the information provided by the spot price spreads in Chapter 2.
The chapter ends with an observation of consumers&#8482; propensity to switch gasoline grades when
prices rise.
</p>
<p>Chapter 6 concludes the report with a more in-depth look at some of the features of gasoline prices:
the Energy Information Administration (EIA) Short-Term Integrated Forecasting System is used to
further quantify the relationship between crude oil and gasoline market factors; the lag relationship
between retail prices and spot prices is quantified; and, finally, an updated outlook for what to
expect for the remainder of the summer is provided.
</p>

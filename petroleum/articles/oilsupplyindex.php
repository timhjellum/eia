<h1>Oil Supply: U.S. Perspective on a Global Market</h1>
<p class="date">January 1, 1996<br />
</p>
<p>The U.S. is heavily reliant on the world crude oil market, which has been subject to huge inter-annual volatility since 1973. Neither of these facts is likely to change. Domestic crude oil production declined over the past decade, while domestic crude oil demand increased. The difference was satisfied by increased crude oil imports. The United States&#8482; proved crude oil reserves declined more than 21 percent from 1985 to 1995. Its technically recoverable crude oil resources beyond proved reserves are estimated to be about 6 times more than the year-end 1995 proved reserves. However, excepting the Gulf of Mexico and the Alaskan offshore, many of the most promising oil-prone regions of the country are presently off-limits to exploration. Over the long term â€” beyond 2020 or so â€” the United States will be increasingly unable to satisfy its crude oil requirements from domestic sources. Imported volumes and world oil prices can both be expected to rise over time, and much of the new imports ill have to be btained from the Persian Gulf region.
</p>
<p>The market for crude oil is global. To varying degrees, every continent on Earth except Antarctica is both a producer and a consumer of crude oil. For a host of reasons having to do with factors such as how supply and demand evolved over time relative to the location and discovery sequence of commercially exploitable conventionally reservoired deposits, as well as rates of progress in the geosciences and petroleum engineering, the price of crude oil remained remarkably stable from the early 1900's through 1973 at less than $15 per barrel in constant 1992 U.S. dollars. For much of the time prior to 1959 the real (inflation adjusted) price was substantially less.
</p>
<p>After 1973 the coupling of significant concentrations of supply-side market power with short-term inelasticity of demand and regional conflicts rendered the world crude oil market subject to huge inter-annual volatility. The resulting post-1970 world crude oil price path, represented by the nominal U.S. refiner acquisition cost of imported crude oil, is shown in Figure 36. It is annotated with significant events that affected the market, the U.S. actions taken in response to them, and the domestic environmental and energy conservation measures referred to or discussed in the other chapters. In keeping with the other chapters&#8482; short-term horizon, the following sections of this chapter primarily address the past decade, which began with the crude oil price collapse of 1986.

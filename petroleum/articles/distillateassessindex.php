<h1>Distillate Fuel Oil Assessment for Winter 1996-1997</h1>
<p class="date">June 15, 1997<br />
</p>
<p>Under assumptions of normal weather, demand for distillate fuel
oil this heating season (October 1, 1996 - March 31, 1997) is
projected to be only slightly greater than that of last winter. Unusually low distillate stocks, however, are expected to result in a tighter-than-normal supply situation and higher heating oil prices. This article describes findings of an analysis of the current low level of distillate stocks which are available to help meet the demand for heating fuel this winter, and presents a summary of the Energy Information Administration&#8482;s distillate fuel oil outlook for the current heating season under two weather scenarios</p>
<p><strong>Distillate Stocks</strong></p>
<p>An analysis of longer term historical trends in distillate fuel oil
inventories appeared in the June issue of the <em>Petroleum Marketing
Monthly.</em> It showed that distillate stocks were on a generally declining path
in the 1980&#8482;s. This was largely because of petroleum industry
downsizing and improving efficiencies in inventory management.
That trend reversed in 1990, and stock levels were generally rising
through 1994. Three factors are cited as possible causes of the
turnaround: (1) Stocks were depressed by a series of colder than
normal winters in the late 1980&#8482;s; (2) the Persian Gulf War in
1990-91 may have contributed to precautionary inventory
building; and (3) environmental regulations imposed by the Clean
Air Act Amendments of 1990 reduced the fungibility of diesel fuel
and heating oil inventories. In contrast to the shift in trend in
distillate stocks, gasoline and crude oil inventories have gradually
declined throughout the 1980&#8482;s and the 1990&#8482;s.
</p>
<p>The purpose of this analysis is to provide a shorter term perspective
on the exceptionally low level of distillate fuel oil inventories as we enter the 1996-97 heating season, and to identify
some contributing causes and potential implications of
the situation.
</p>
<p><strong>Background</strong> </p>
<p>This has been a year of unusual market behavior with
regard to petroleum products. Earlier this year, gasoline
prices in the United States rose sharply from $1.08 in
mid February to $1.29 by May 17. In response to public
concern, President Clinton, on April 29, asked that the
Department of Energy investigate the reasons for the
runup, and prepare a report on the situation within 45
days. The report, published in June, concluded that
crude oil price increases and normal seasonal gasoline
price increases accounted for most of the change, but that
unusual factors in gasoline markets also played a role.
These unusual factors included: a late-winter cold spell
causing refiners to focus longer than usual on production
of distillate instead of gasoline; lower-than-normal
gasoline stocks; high gasoline demand and little excess
refining capacity; and persistent expectations that crude
oil and gasoline prices would fall in the future, which
discouraged building stocks. Now, at the beginning of
the heating season, distillate fuel has replaced gasoline
as the focus of concern.
</p>

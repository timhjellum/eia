<h1>Oil and Gas Resources of the West Siberian Basin, Russia</h1>
<p class="date">November 1, 1997<br />
</p>
<p>Oil and Gas Resources of the West Siberian Basin,
Russia is part of the Energy Information Administration&#8482;s
(EIA&#8482;s) Foreign Energy Supply Assessment Program
(FESAP). The primary objective of this study is to assess
the oil and gas potential of the West Siberian Basin of
Russia. The study does not analyze the costs or
technology necessary to achieve the estimates of the
ultimate recoverable oil and gas.
</p>
<p> This is the second report on an oil and gas province in
the former Soviet Union. The first, <em>Oil and Gas
Resources of the Fergana Basin (Uzbekistan,
Tadzhikistan, and Kyrgyzstan)</em>, was published in January
1995.
</p>
<p>Russia&#8482;s West Siberian Basin contains sufficient oil and
natural gas to affect world petroleum markets. The basin
supplies approximately 70 percent of the oil and 90
percent of the gas production for Russia. Decreases in
produced volumes from the basin would require that the
demand for petroleum in Russia be met from other
sources. Likewise, increases in production would
increase the amount of oil or gas for export into
international markets.
</p>
<p>This study uses reservoir data to estimate recoverable oil
and gas quantities which were aggregated to the field
level. Field totals were summed to a basin total for
discovered fields. An estimate of undiscovered oil and
gas, from work of the United States Geological Survey
(USGS), was added to give a total basin resource volume.
Recent production decline points out Russia&#8482;s need to
continue development of its discovered recoverable oil
and gas. Continued exploration is required to discover
additional oil and gas that remains undiscovered in the
basin.
</p>
<p>The estimates of recoverable oil and gas for the regions
of the basin, the individual fields, and the geologic
intervals are intended as a guide to organizations such as
oil and gas operating companies, financial institutions,
and government agencies. In addition to the estimates of
maximum recovery, the reported and estimated reservoir
parameters are a valuable reference source. The
appendices contain the estimates of ultimate recovery by
field and frequency distributions of the reservoir data.
Additionally, a computer diskette contains the reported
and estimated parameters for each productive reservoir.
</p>
<p>Several independent petroleum engineers and geologists
with experience in West Siberia have reviewed the
analysis and contributed many useful suggestions which
have been incorporated in the report. Retired and current
staff of the USGS also assisted in reviewing this report.
The assistance of all of these professionals is gratefully
acknowledged. Special thanks is given to Dr. James W.
Clarke and Dr. James A. Peterson, USGS retired, who
contributed Appendix A, Petroleum Geology in its
entirety.
</p>

<h1>Future Oil Production for the Alaska North Slope</h1>
<p class="date">May 1, 2001<br />
</p>
<p><em>Future Oil Production for the Alaska North
Slope</em> was prepared by the Reserves and
Production Division, Office of Oil and Gas,
Energy Information Administration (EIA) as
part of the Domestic Oil and Gas Technical
Support Program. The report presents a range of
plausible production scenarios of combined
crude oil, condensate, and natural gas liquids
(NGL) <em>hereafter jointly referred to as oil</em>, for the
Alaska North Slope (North Slope or ANS).
</p>
<p>Production of oil (total liquids) from the ANS
has declined from the 1988 peak rate of over 2.0
million barrels per day (MMbbl/d). As of this
writing, the rate of production is about 1.1
MMbbl/d, accounting for 16 percent of total
domestic production in 2000. Cumulative
production through 2000 was 13.306 billion
barrels (Bbbls).
</p>
<p>In the absence of additional development 3.707
Bbbls remain to be produced from the currently
on-stream fields. The inclusion of identified
development, defined for the purposes of this
report as projects to develop additional resources
in discovered fields located near the existing
production and transportation infrastructure,
increases the ANS remaining recovery potential
to 5.126 Bbbls.
</p>
<p>Beyond identified development, exploration for
and subsequent development of as-yet
undiscovered ANS resources could add
significant volumes to ultimate recovery. Three
estimates of undiscovered technically
recoverable resources that are representative of
the range of uncertainty associated with the
resource estimation process are used herein to
provide a range of production scenarios for each
of the ANS sub-areas analyzed. These estimates
are based on the U.S. Geological Survey&#8482;s
(USGS) 95 percent probability (19 in 20 chance)
of occurrence, mean (expected value), and 5
percent probability (1 in 20 chance) of
occurrence values.
</p>
<p>The production projections presented here were
constructed in three layers beginning with the
anticipated future production from currently on-stream fields. The second layer adds production
from the anticipated development of identified
fields and projects. The third layer adds
projected volumes of production from
technically recoverable undiscovered resources.
The projections are based on historical
performance, the reported anticipated
performance of projects in the identified
development category, and the application of
current performance parameters to estimates of
undiscovered resources.
</p>
<p>The assumed minimum and maximum operating
capacities of the Trans Alaska Pipeline System
(TAPS) were limiting factors in determining
minimum and peak production rates. Production
projections based on performance history such
as those presented in this report inherently
embody past rates of technology improvement.
The production possibilities associated with
potential future gas-to-liquid conversion, gas
liquefaction, and high viscosity crude production
projects were not considered in this analysis.
</p>
<p>The range of remaining and undiscovered
Alaska North Slope oil is 6.1 to 13.3 billion
barrels with a mean (expected) value of
approximately 8.9 billion barrels. Ultimate
recovery will range from 19.4 to 26.6 billion
barrels. The mean ultimate recovery is
approximately 22.2 billion barrels. Figure ES1
shows the production schedule for the mean
estimates of undiscovered resources layered on
the projected production of the currently
producing and anticipated new development
fields.
</p>
<p>Identified development will stabilize ANS
production at approximately 1.0 MMbbl/d until
2005, at which point decline resumes in the
absence of new discoveries. If the identified
development is delayed by factors such as low
oil prices or environmental issues, production
will begin declining before the year 2005.
</p>
<p>Finding and developing the estimated mean
volume of undiscovered resources in the ANS
areas that are presently open to exploration and
development activity maintains the production rate at approximately 1.0 million barrels per day
until 2010. Production from the development of
undiscovered resources in the National
Petroleum Reserve-Alaska (NPR-A) is assumed
to begin in 2010, causing production to remain
close to 1.0 million barrels per day for another 5
years. The development of the estimated mean
volume of resources increases the productive life
of the ANS by approximately 10 years.
Development of volumes larger than the mean
estimate could increase production to
approximately 1.4 million barrels per day.
</p>
<p>Continued exploration for and development of
undiscovered ANS resources is necessary to
make a significant impact on production decline.
Since minimum ANS production is not based on the minimum economic limits of individual
fields, but rather on the sum of production from
all producing fields compared to the assumed
minimum pipeline operating limit, the timing of
the future development will become critical as
production declines to approach that limit. To
the extent that development is delayed,
technically recoverable oil resources in known
fields will not be recovered. Further, when new
development is added late in the producing life
of the area, it might not be possible to produce
the newest fields to their economic abandonment
rate before the pipeline reaches its minimum
operating limit and is shut down, in which event
the remaining reserves in these fields also would
not be recovered.
</p>

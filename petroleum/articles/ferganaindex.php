<h1>Oil and Gas Resources of the Fergana Basin (Uzbekistan, Tadzhikistan, and Kyrgysztan)</h1>
<p class="date">December 1, 1994<br />
</p>
<p>This analysis, prepared in cooperation with the U.S.
Geological Survey (USGS), is part of the Energy Information
Administration&#8482;s (EIA&#8482;s) Foreign Energy
Supply Assessment Program (FESAP). While past FESAP
analyses cover most of the major oil and gas
provinces of the world, this one for the Fergana basin
is an EIA first for republics of the former Soviet Union
(FSU). This was a trial study of data availability
and methodology, resulting in a reservoir-level assessment
of ultimate recovery for both oil and gas. Ultimate
recovery, as used here, is the sum of cumulative
production and remaining Proved plus Probable reserves
as of the end of 1987. Reasonable results were
obtained when aggregating reservoir-level values to
the basin level, and in determining general but important
distributions of across-basin reservoir and fluid
parameters. Plans are underway for other assessments
of basins in the FSU. However, future FSU basin assessments
by EIA will probably be based on fieldlevel
detail.
</p>
<p>A 14-page Advance Summary of this analysis was
published in December 1993. With its computer diskette
of spreadsheet data files, the summary was available
prior to the announced January 1994 close of
competitive bidding in Uzbekistan, for oil and gas license
blocks covering most of the basin. In July 1994,
the republic of Kyrgyzstan also offered oil and gas
blocks for bid. While no Kyrgyz blocks were offered
in the Fergana basin by December 1994, such an offering
is expected. Kyrgyzstan has announced its intention
to offer Fergana basin blocks within the next few
years. Currently, this report represents the most comprehensive
assessment publicly available for oil and
gas in the Fergana basin. Unfortunately, no new data
have been added to those used for the Advance Summary.
This full report uses the same engineering data
as collected and estimated for the Advance Summary,
including spreadsheet data on the accompanying diskette.
This full report provides additional descriptions,
discussions and analysis illustrations that are beneficial
to those considering oil and gas investments in the
Fergana basin.
</p>

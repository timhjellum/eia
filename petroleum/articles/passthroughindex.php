<h1>Regional Comparisons, Spatial Aggregation, and Asymmetry of Price Pass-Through</h1>
<p class="date">August 3, 2005<br />
</p>
<p><strong>Introduction
</strong></p>
<p>In the petroleum industry, the majority of all products sold changes hands a number
of times on its way from the point of production at a refinery to the point of ultimate
consumption. Since each participant in this supply and marketing chain incurs some cost
and wishes to make a profit, the price normally increases with each intermediate sale. As
such, any change in price at the refinery, or at any intermediate point of sale
downstream, would be expected to affect prices at each successive sale. This is the socalled
price pass-through in the economic literature.
</p>
<p>In this paper, wholesale spot price to retail price pass-through in the gasoline market
was investigated for several regions and subregions, as well as for the aggregate United
States. Because all marketing channels culminate in retail sales competing side-by-side
on the street, it is possible for many analytical purposes to focus on spot and retail prices
only, with the difference between them representing the costs and profits of the
distribution and marketing sector.
</p>
<p>This subject is of interest to many audiences: the general public, the petroleum
industry, and policy makers. The energy statistic most visible to American consumers is
the retail price of gasoline. When substantial price changes occur, especially upward,
there are often allegations of impropriety on the part of petroleum refiners and/or
marketers. Traders and other industry participants would be interested since the
relationships between spot and retail prices are consistent and predictable, to such an extent that changes in spot prices may be used to forecast subsequent changes in retail
prices for the various regions. Issues related to price pass-through can be useful to policy
makers, regulators, and administrators in evaluating market performance at the regional
and national level.
</p>
<p>The economic literature shows great interest in the pass-through phenomenon on
many subjects, such as in international trade for the pass-through of exchange rates or
tariffs to export and import prices [Parsely, 2001], in monetary policy for the passthrough
of money supply to price inflation [Smets and Wouters, 2002], in fiscal policy for
the pass-through of tax changes, in manufacturing for the pass-through of wages to
output price [Peltzman, 2000], and for the product price pass-through of many
agricultural commodities [Goodwin and Holt, 1999].
</p>
<p>Gasoline price pass-through draws particular attention both in the empirical and
theoretical literature [Duffy-Deno, 1996; Borenstein et al., 1997; Dale et al., 1999; Lewis,
2003; Sen, 2003; Zyren and Shore, 2003]. Economists are concerned whether price passthrough
is symmetric or asymmetric. The asymmetry, if any, can be categorized into two
types: amount asymmetry, in which the magnitude of changes at the wholesale and retail
level are compared, and pattern asymmetry, which is concerned with different passthrough
speeds for rising and falling prices. Much of the literature deals with the pattern
asymmetry issue, testing its existence and explaining the factors causing any detected
pattern asymmetry.
</p>
<p>Spatial aggregation issues of price pass-through have been mentioned in the
literature, but to our knowledge have not been thoroughly investigated, particularly in
gasoline markets. While some cross-country comparisons of price pass-through behavior
have been found in the literature [Galeotti et al., 2003], no intracountry regional
comparisons have been found.
</p>
<p>The purpose of this work is to study U.S. national and regional wholesale to retail
price pass-through in the gasoline market and to explore the possibility of pattern
asymmetry and spatial aggregation effects. The focus is on the short-run pattern
symmetries over five Petroleum Administration for Defense Districts and three
subdistricts on the East Coast. Asymmetry was found in pass-through patterns for all
regions, subregions, and at the U.S. national level. Pass-through patterns were found to be
different from region to region in terms of speed and the number of weeks required for
complete pass-through. Regarding spatial aggregation, the results show that for spot price
increases, the aggregated cumulative pass-through is greater than the corresponding
volume-weighted, composite, regional, and subregional pass-through.
</p>
<p>In the next section, the data used in this paper are described. Pattern asymmetry,
regional comparisons, and aggregation issues are analyzed in the following section, using
asymmetric model specifications with error correction terms. Conclusions are drawn in
the final section, together with several suggestions for future work. Supporting materials
and results are organized in appendices at the end of the paper.
</p>

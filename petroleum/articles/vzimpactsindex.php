<h1>Impacts of the Venezuelan Crude Oil Production Loss</h1>
<p class="date">September 1, 2003<br />
</p>
<p>The loss of almost 3 million barrels per day of crude oil production in Venezuela
following a strike in December 2002 resulted in an increase in the world price of crude
oil. However, in the short term, the volume loss probably affected the United States more
than most other areas. This country receives more than half of Venezuela&#8482;s crude and
product exports, and replacing the lost volumes proved difficult.
</p>
<p>U.S. imports of Venezuelan crude oil dropped significantly in December 2002 relative to
other years. Increases in imports from Mexico and the Middle East,
particularly Iraq, helped to cushion the decline, but import volumes were down in
general.
</p>
<p>Venezuela is only about 5 days away from the U.S. Gulf Coast by tanker. Most of the
Venezuelan crude oil used in the United States is heavy, sour quality crude oil,2
and other
nearby sources of similar crude oil, such as Mexico, have little additional capacity to
increase supply in the short term. Crude oils from sources somewhat farther away, such
as West Africa or the North Sea, are mostly lighter and more expensive than the lost
Venezuelan volumes. When the heavier Venezuelan supply was disrupted, these lighter
crude oils may not have been economically attractive to refiners previously using the
Venezuelan crude oils. While refineries using heavy Venezuelan crude oils theoretically
can use some lighter crude oils from areas like West Africa, their refineries are designed
to run most economically with the heavier crude oils.
</p>
<p>The largest number of refineries in the United States can process light, sweet crude oils,
while only the small fraction of refineries that have extensive desulfurization and
bottoms-conversion units can use heavy, high sulfur crude oils such as that produced in
Venezuela. Refineries that normally run intermediate sour crude oil could run a small
quantity of heavy sour crude if they could blend it with light sweet crude oil, but if light
sweet crude oil is in short supply, then that option is not available. When a heavy sour
production source is disrupted, refiners can run a lighter mix of crude oils, but as in the
recent Venezuelan production loss, refiners shift crude oils so that the heavy crude oil
refiners still run a relatively heavier mix. Both acquisition of additional crude oils and
the shifting takes time, and runs will generally be reduced for a short time.
</p>
<p>Ultimately, OPEC was the only supplier capable of increasing production adequately to
cover for the losses, and OPEC members like Saudi Arabia, Kuwait, and Iraq have crude
oils similar in quality to the Venezuelan crude oils. But OPEC&#8482;s Middle Eastern crude
oils are 30-40 days away, leaving a regional short-term supply gap in the United States.
</p>
<p>This assessment of the Venezuelan petroleum loss examines two areas. The first part of
the analysis focuses on the impact of the loss of Venezuelan crude production on crude
oil supply for U.S. refiners who normally run a significant fraction of Venezuelan crude
oil. It explores the extent to which refineries normally using Venezuelan crude were able
to acquire alternative supply and how these refineries were affected depending on their
affiliation with Venezuela&#8482;s national oil company, PetrÃ³leos de Venezuela, SA (PDVSA),
and the quality of Venezuelan crude oils being used. The second part of the analysis
looks at the impact of the Venezuelan production loss on crude markets in general, with
particular emphasis on crude oil imports, refinery crude oil throughput levels, stock
levels, and the changes in price differences between light and heavy crude oils.
</p>
<p>EIA&#8482;s findings and supporting data are discussed in greater detail below and in the body
of this report.
</p>

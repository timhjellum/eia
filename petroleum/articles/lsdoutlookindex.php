<h1>Demand, Supply, and Price Outlook for Low-Sulfur Diesel Fuel </h1>
<p class="date">August 1, 1993<br />
</p>
<p>To help ensure that sulfates in engine exhaust do not
prevent manufacturers of heavy-duty diesel engines
from meeting new particulate emissions standards for
1994 and later model years, the Clean Air Act Amendments of 1990 (CAAA90) require refiners to reduce the sulfur content of on-highway diesel fuel from current average levels of 0.30 percent by weight to no more than 0.05 percent by weight. The new standard, which goes into effect October 1, 1993, also requires that on-highway diesel fuel have a minimum cetane index of 40 or a maximum aromatic content of 35 percent by volume. This provision is designed to prevent any future rises in aromatics levels. Since the direct measurement of aromatics is complex, a minimum cetane index of 40 is specified as a surrogate for capping aromatics. Except for California refineries, the level of aromatics does not appear to be a critical issue.
</p>
<p>Home heating oil, a distillate product similar to diesel
fuel, is not required to meet the new specifications.
Off-highway diesel fuel and heating oil not meeting the
new standards must be dyed in order to distinguish them
from on-highway diesel fuel. The new diesel fuel standard
applies nationwide and affects about 46 percent of
the total domestic demand for distillate fuel oil (about
8.2 percent of total U.S. petroleum product demand).
</p>
<p>Refiners will incur higher operating and capital costs in
adapting to the new demand pattern for low-sulfur diesel fuel. These increases will likely yield a price premium of 3 to 4 cents per gallon over heating oil and other high-sulfur distillate fuels. In California, the price premium will probably be higher because of a more restrictive aromatics specification, although no projection is attempted here.
</p>
<p>This article analyzes the new regulations&#8482; impact on
distillate fuel markets and evaluates the constraints and
costs faced by the petroleum refining industry in complying
with them. The article reviews recent trends in
demand and quality of on-highway diesel fuel oil and
presents short- and long-term forecasts of diesel fuel
demand. It discusses domestic and foreign sources of
low-sulfur diesel supply and then moves on to an analysis
of distillate fuel desulfurization economics, including
diesel fuel price seasonality, desulfurization
variable and capital costs, sulfur dioxide (SO2
) emissions
trading allowances, and the economics of blending
lighter, more expensive petroleum fractions, such as
kerosene, into diesel fuel. The article concludes with a
brief review of events in Los Angeles in January 1985,
when the California Air Resources Board reduced the
sulfur specification for diesel fuel from 0.5 to 0.05
percent by weight.
</p>

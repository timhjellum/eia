<h1>Petroleum: An Energy Profile 1999 </h1>
<p class="date">July 1, 1999<br />
</p>
<p>Petroleum has become so interwoven in the daily lives of
Americans over the past century that it now comprises an industry
which accounts for about 40 percent of all energy consumed
in the United States. Americans use petroleum,
directly or indirectly, in many applications including transportation
fuel, light, heat, and for use in the manufacture of
consumer products. Daily per capita consumption of petroleum
products in the United States averages nearly 3 gallonsâ€”more
than one-fourth of the petroleum consumed
worldwide.
</p>
<p>Petroleum is a complex mixture of liquid hydrocarbonâ€”chemical
compounds containing hydrogen and carbonâ€”occurring
naturally within the earth. Early use of
petroleum dates back many years. Man first became aware
of its existence when it was discovered oozing to the surface
from natural "seeps."? Native Americans used crude oil
skimmed from creeks and rivers as a medicinal ointment. In
the 1840&#8482;s and 1850&#8482;s, an enterprising Pennsylvanian bottled
and sold "Pennsylvania Rock Oil"? to treat ailments such as
rheumatism, gout, and burns. By the time commercial production
began in 1859, oil was demanded more for its ability
to produce light, and kerosene for use in lamps was the principal
product. At the turn of the century, the advent of the
automobile shifted the emphasis to motor gasoline. In the 1930&#8482;s and 1940&#8482;s, a substantial market for heating oil
developed.
</p>
<p>Because petroleum in its raw state has limited uses, further
processing of crude oil, via petroleum refining, is needed to
unlock the full potential of this resource. In the mid-1800&#8482;s,
the earliest refineries used a distillation process to produce
kerosene. Today&#8482;s refineries employ many processes from
simple distillation to the more complex "cracking"? and "reforming"?
operations to convert crude oil into a wide array of
desired products. With approximately 20 percent of the
world&#8482;s crude oil distillation capacity and about 30 percent of
the more complex cracking and reforming capacity, the
United States leads the world in the production of petroleum
products. Moreover, motor gasoline alone accounts for about
one-half of all U.S. refinery production.
</p>
<p>Although petroleum provides many useful products, the
most notable are motor gasoline and heating fuel. Petroleum&#8482;s
many uses in the transportation sector include fuel for
automobiles, trucks, agricultural and industrial machinery,
trains, ships, and aircraft. Petroleum is used to heat homes,
offices, and factories and is used to grow, process, package,
distribute, refrigerate, and cook food. Petroleum is also the
source of synthetic fabric for cloths as well as detergents and dry cleaning solvent to clean them. Moreover, petroleum
provides a chemical base for cosmetics and pharmaceutical
products as well as for many plastic products from toys to
building materials.
</p>
<p>The United States consumes more energy from petroleum
than from any other energy source. Petroleum provides
nearly two-thirds more energy than natural gas, nearly
three-fourths more energy than coal, and more than three
times the energy from all other energy resources. Although
the United States is one of the largest petroleum producers, it
actually consumes more than it produces, requiring net imports
of crude oil to meet demand.
</p>
<p>Growing U.S. petroleum product demand and declining domestic
crude oil production have combined to make the
United States increasingly dependent on crude oil imports.
From its peak of 9.6 million barrels per day in 1970, domestic
crude oil production had declined by nearly one-third to 6.5
million barrels per day by 1997. During this same period, petroleum
demand grew by one-fourth, from 14.7 million barrels
per day in 1970, to over 18.6 million barrels per day by
1997. Consequently, net imports of crude oil and products
rose from 3.2 million barrels per day in 1970, to 9.2 million
barrels per day by 1997, causing U.S. dependence on petroleum
net imports to grow from about one-fifth in 1970, to
nearly half by 1997.
</p>
<p>While crude oil production is declining in the United States,
so are proved reserves of crude oil. The U.S. holds about a 2
percent share of the world&#8482;s crude oil reserves. As world reserves
are depleted, the roles of Saudi Arabia and other Middle
East suppliers are increasingly important in international
petroleum supply. This was dramatically illustrated by fears
of worldwide supply disruption following Iraq&#8482;s invasion of
Kuwait in August 1990.
</p>
<p>A vast transportation network of tankers, barges, pipelines,
railways, and trucks moves crude oil from the field to refineries,
where it is processed, and brings refined products to the consumer. This complex system also links foreign suppliers to domestic customers and provides access to foreign markets
for products from U.S. refineries.
</p>
<p>Storage facilities hold crude oil and refined products at various
stages as they move through the system. Marine terminals
receive crude oil as it is unloaded from tankers and hold
it until it is transported by pipeline or other means to the refinery
for processing. When it reaches the refinery, it is
stored again until it can be processed. After processing, the
refined products go to bulk terminals where they are stored
until they are transferred to retail sales outlets and the final
consumer. Above-ground tanks, underground caverns and
tanks, and offshore storage are used to hold supplies of crude
oil and petroleum products as they move through the distribution
system. The largest underground storage facilities are
part of the U.S. Strategic Petroleum Reserve, containing petroleum
stocks maintained by the Federal Government for
use during periods of major supply disruptions.
</p>
<p>The petroleum industry can be divided into two main sectors
that are largely functional in nature: the upstream sector that
includes exploration and production and the downstream
sector which includes refining, transportation, and marketing.
The industry is highly integrated, with many firms involved
in more than one sector. Large companies known as
"majors"? are fully integrated and may own and operate establishments
involved in all of these sectors. Smaller, nonintegrated
companies often referred to as "independents"?
generally specialize in one aspect, such as crude oil exploration
and production or product marketing. The petroleum industry
is international in scope, and it has a complex system
of markets where exchanges, long- and short-term sales contracts,
and spot market and futures contracts are used to
transfer oil ownership as it passes from one sector to the next.

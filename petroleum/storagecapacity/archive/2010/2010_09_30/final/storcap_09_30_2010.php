<p>
Working and Net Available Shell Storage Capacity as of September 30, 2010 is the Energy Information Administration's (EIA) first report containing semi-annual storage capacity data. It includes three tables detailing working and net available shell storage capacity by facility type, product, and PAD District as of September 30, 2010.  EIA has reported weekly and monthly inventory levels of crude oil and petroleum products for decades.  New storage capacity data can help analysts place petroleum inventory levels in context and better understand petroleum market activity and price movements, especially at key market centers such as Cushing, Oklahoma.<br><br>
Previously, petroleum storage capacity data from EIA were limited to annual reports at refineries.  Now, in addition to refineries, storage capacity includes capacity at petroleum products terminals, fuel ethanol plants, product pipelines (tank and underground storage), and crude oil tank farms.  <br><br>
Data will now be collected as of March 31 and September 30 each year.  This schedule allows for more frequent updates to track changes in overall storage capacity and also allows for tracking seasonal shifts in petroleum product usage of tanks and underground storage.  <br><br>
Using the new storage capacity data, it will be possible to calculate approximate storage capacity utilization rates at refineries and petroleum products terminals.  Unfortunately, meaningful storage capacity utilization rates for operators of crude oil tank farms and pipelines, or for operators of products pipelines, cannot be readily calculated at this time.  This is due to the fact that existing data on inventory levels at these facilities is reported on a different basis than the new storage capacity data.  To address this, EIA is considering updates of crude oil and products pipeline surveys to require semiannual reporting of inventory levels on the same basis as storage capacity.  This will enable data users to calculate storage capacity utilization rates for crude oil tank farms and pipelines as well as products pipelines.
<br><br>
Additional information and explanations related to the data are found in <a href="/oog/info/twip/twiparch/101201/twipprint.html">This Week in Petroleum</a>.

<p>
<strong>The U.S. Energy Information Administration issued the following advisory on the reporting error in the combined propane stocks for PADDs 4 and 5 that occurred in today's Weekly Petroleum Status Report.</strong><br /><br />
After today's publication of the Weekly Petroleum Status Report (WPSR) for the week ending June 7, 2013, an error was discovered in the reported combined propane stocks in PADDs 4 and 5. The reported number of 2.114 million barrels, an increase of 951,000 barrels from last week, is incorrect.<br /><br />
This was due to an error in reported data that was not caught by our analysts before publication. Normal week-to-week increases for this region at this time of year would be in the range of 80,000 barrels to 200,000 barrels. At this time we do not have a restatement of the stock data for PADDs 4 and 5. If there is a revision, the updated numbers will be included in the next WPSR that will be released on June 19.<br /><br />
The WPSR revision policy states "EIA disseminates revised weekly data only if the revision is expected to substantively affect users understanding of U.S. petroleum prices. The decision to disseminate a revision to weekly data will be based on EIA's judgment of the revision's expected effect. If a revision is necessary, it will be disseminated in the next regularly scheduled release of the weekly products."<br /><br />

The U.S. Energy Information Administration re-released petroleum supply data on October 6, 2016 to make the following corrections to data in interactive Petroleum Data Tables.  Data on Petroleum Supply Monthly and Petroleum Supply Annual tables in portable document and comma-separated values formats were correct as posted on September 30, 2016 and were not changed.

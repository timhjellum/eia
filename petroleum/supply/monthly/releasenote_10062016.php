<strong>Released: September 30, 2016</strong></p>
<p>
The U.S. Energy Information Administration released the following data on September 30, 2016.
<ul>
<li><a href="/petroleum/supply/monthly/">Petroleum Supply Monthly (PSM)</a> with data for July 2016</li>
<li>Petroleum Supply Annual (PSA) <a href="/petroleum/supply/annual/volume1/">Volume 1</a> and <a href="/petroleum/supply/annual/volume2/">Volume 2</a> with revised data for January-December 2015</li>
<li>Revised petroleum export and product supplied data for June 2016 (see note)</li>
<li>Revised tanker and barge movements of crude oil from Petroleum Administration for Defense District (PADD 3) to PADD 1 beginning with data for January 2013</li>
<li>Revised rail movements data beginning with data for January 2010</li>
<li>Revised crude oil production beginning with data for January 2006</li>
<li>Removed crude oil lease stocks (crude oil stocks held at producing sites) from total crude oil stocks beginning with data for January 2005</li>
</ul>
Note:  The initial release of PSM data for June 2016 excluded 1,001 thousand barrels of exports of petroleum products from the 50 states and District of Columbia (PADDs 1-5) to Puerto Rico and the U.S. Virgin Islands.  These exports were added to data for June 2016 in Petroleum Data Tables and product supplied was recalculated using the revised export quantity.  PSM tables in Portable Document Format (PDF) with data for June 2016 were not updated.
</p>

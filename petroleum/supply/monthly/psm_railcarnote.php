<h3>New Propane, Propylene, Butane and Isobutane Movements by Rail added to the Petroleum Supply Monthly</h3>
<p style="font-size:11px;margin:0px;padding:4px 0px;color:#7d7d7d;">
<strong>Released: January 31, 2017</strong></p>
<dl class="survey">
<p>
Starting today with the release of <a href="/petroleum/supply/monthly/"><em>Petroleum Supply Monthly</em></a> (PSM) data for November 2016, the U.S. Energy Information Administration (EIA) included new data on selected hydrocarbon gas liquids (HGLs) including propane, propylene, butane and isobutane transported by railroads. EIA now reports monthly rail movements for crude oil, propane, propylene, butane, isobutane, ethanol, and biodiesel.
</p>
<p>
Adding movements of propane, propylene, butane and isobutane by rail to existing movements between Petroleum Administration for Defense Districts (PADDs) by pipelines, tankers, and barges provides more complete data on inter-PADD movements of HGLs. In addition, incorporating propane, propylene, butane and isobutane movements into regional volumetric balances also improves regional product supplied estimates for these HGLs.
</p>
<p>
In addition to data for November 2016, EIA updated movements and regional balances in interactive tables starting with data for January 2010. PDF tables were not updated for months prior to November 2016.
</p>
<p>
PSM tables affected include tables 5-24, 57, 60, and 62.
</p>
<p>

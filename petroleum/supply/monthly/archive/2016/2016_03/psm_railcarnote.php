<p>
Starting today with the release of <a href="/petroleum/supply/monthly/">Petroleum Supply Monthly</a> (PSM) data for January 2016, the U.S. Energy Information Administration (EIA) included new data on ethanol and biodiesel transported by railroads.  EIA now reports monthly rail movements for crude oil, ethanol, and biodiesel.

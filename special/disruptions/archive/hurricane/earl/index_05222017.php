                      <td width="47%" valign="top" class="data" style="padding-top:15px; padding-bottom:15px;"><h2>September 1, 2010</h2>
                        <p>The National Oceanic and Atmospheric Administration (NOAA) reported  that, as  of 2:00 p.m. EDT, September 1, 2010, Hurricane Earl was  centered about 275 miles east of Abaco Island, Bahamas, and about 680 miles  south-southeast of Cape Hatteras, North Carolina.  The storm had sustained winds of 125 miles  per hour (Category 3), and was moving northwest at approximately 17 miles per  hour.</p>
<p>Hurricane Earl is a large and powerful hurricane, and its  current forecast track would take it near the U.S. East Coast at Cape Hatteras,  then remaining offshore, but moving up the coast past New England to make  landfall in Nova Scotia, Canada by Saturday, September 4. The National Weather Service has issued a  Hurricane Warning for much of the North Carolina  coast, and a Hurricane Watch from the North Carolina/Virginia border to Cape Henlopen, Delaware.  Further watches and warnings are expected for  the U.S. Atlantic coast as the storm progresses northward.</p>
                      <p>The projected path of Hurricane Earl has significance for  energy supply because of its proximity to refineries in Yorktown,  Virginia, and the Philadelphia  and New York Harbor areas, along with major seaports  throughout the Eastern Seaboard. A total  of 1.1 million barrels per day of operable refinery capacity, or about 7  percent of the nation's total capacity, lies within the likely affected area.  In addition, strong winds and a potential  storm surge could impact electric power infrastructure throughout the coastal  region.</p></td>

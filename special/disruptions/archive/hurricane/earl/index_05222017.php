                      <td valign="top" class="data" style="padding-top:15px; padding-bottom:15px;"><h2>September 2, 2010 a.m.</h2>
                        <p>The National Oceanic and Atmospheric Administration (NOAA) reported that, as of 5:00 a.m. EDT, September 2, 2010, Hurricane Earl was centered about 410 miles south of Cape Hatteras, North Carolina, and about 870 miles south-southwest of Nantucket, Massachusetts.  The storm had sustained winds of 145 miles per hour (Category 4), and was moving north-northwest at approximately 18 miles per hour.</p>
<p>The projected track of Hurricane Earl has changed little overnight, and it remains likely to pass close to Cape Hatteras, North Carolina and Cape Cod, Massachusetts, before making landfall in Nova Scotia, Canada early Saturday, September 4.  The National Weather Service continues to post a Hurricane Warning to cover most of the North Carolina coastline, while a Hurricane Watch now applies to the Virginia, Maryland, and Delaware coastline, along with Westport to Plymouth, Massachusetts, including Cape Cod, Nantucket, and Martha's Vineyard.  Watches and warnings will continue to be revised as the storm progresses northward.</p>
                      <p> Although Hurricane Earl presents a potential threat to petroleum refineries and seaports, current relatively high inventory levels for most petroleum products should lessen concerns about possible supply outages.  EIA's Weekly Petroleum Status Report (<a href="/oil_gas/petroleum/data_publications/weekly_petroleum_status_report/wpsr.html">WPSR</a>) for the week ending August 27, 2010 showed inventories of gasoline, distillate fuels (including diesel fuel), and jet fuel well above their seasonal average ranges, both for the East Coast and the United States as a whole.  Additionally, the peak summer gasoline demand season typically ends with Labor Day weekend, and the winter heating demand season does not begin until later in the fall, so petroleum demand should be well below maximum levels over the next few weeks.  However, as with any event featuring severe weather, there is a significant likelihood of localized power and transportation outages. </p></td>
                    </tr>

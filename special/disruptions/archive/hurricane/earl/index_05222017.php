                      <td valign="top" class="data" style="padding-top:15px; padding-bottom:15px;"><h2>September 2, 2010 p.m.</h2>
                        <p>The National Oceanic and Atmospheric Administration (NOAA) reported that, as of 2:00 p.m. EDT, September 2, 2010, Hurricane Earl was centered about 245 miles south of Cape Hatteras, North Carolina, and about 720 miles south-southwest of Nantucket, Massachusetts.  The storm had sustained winds of 125 miles per hour (Category 3), and was moving north-northwest at approximately 18 miles per hour.</p>
                      <p>The U.S. Atlantic coast from North Carolina to Maine remains under threat, with the projected track passing close to Cape Hatteras, North Carolina and Cape Cod, Massachusetts, before making landfall near the border between Maine and New Brunswick, Canada on Saturday, September 4.  A National Weather Service Hurricane Warning now covers most of the North Carolina coastline, as well as the Massachusetts coastline south of Boston, including Cape Cod, Nantucket, and Martha's Vineyard.   A Hurricane Watch continues to apply to the Virginia, Maryland, and Delaware coastline.  The Canadian Weather Service has also issued a Hurricane Watch for parts of Nova Scotia.  Watches and warnings will continue to be revised as the storm progresses northward. </p></td>

                      <td valign="top" class="data" style="padding-top:15px; padding-bottom:15px;"><h2>September 3, 2010</h2>
                        <p>Hurricane Earl has weakened considerably, but remains a threat to parts of the Atlantic coastline in the northeastern United States and Canada.  The National Oceanic and Atmospheric Administration (NOAA) reported that, as of 2:00 p.m. EDT, September 3, 2010, Hurricane Earl was centered about 290 miles south-southwest of Nantucket, Massachusetts.  The storm had sustained winds of 80 miles per hour (Category 1), and was moving north-northeast at approximately 21 miles per hour.  Hurricane Earl passed near Cape Hatteras overnight, causing some flooding, road and ferry closures, and power outages, though no casualties or major property damage have yet been reported.</p>
                        <p>The National Weather Service has reduced the Hurricane Warning area to cover only Cape Cod, Nantucket, and Martha's Vineyard in Massachusetts, while a Tropical Storm Warning is in effect for much of the remaining U.S. coastline from Virginia north.  The Canadian Weather Service has also issued a Hurricane Watch for parts of Nova Scotia.  Watches and warnings will continue to be revised as the storm progresses northward. </p>
                      </td>

<p><strong>Final Report<br />
November 9, 2012 </strong><br />
The U.S. Energy Information Administration (EIA) estimates that 28% of gas stations across the New York City metropolitan area do not have gasoline available for sale today, same as yesterday and down from 67% last Friday when EIA began its emergency survey. Despite the improvement since last Friday, the 28% remains a significant problem. Some counties in New Jersey, New York City, and Nassau and Suffolk counties have addressed this issue by imposing license plate allocation of gasoline.  For further information on efforts to provide gasoline supplies to the affected area, please go to the Department of Energy's <a href="http://energy.gov/articles/helping-local-officials-address-fuel-shortages">Helping  Local Officials Address Fuel Shortages</a> page.</p>
<p>Hurricane Sandy struck the U.S. East Coast on Monday, October 29, 2012. As a result of the subsequent disruptions to the supply chain for gasoline, EIA implemented an emergency survey starting on Friday, November 2, 2012, to monitor the vehicle fuel supply conditions in the New York City metropolitan area on a temporary basis. Today's report is the last for this emergency survey.</p>
<p>Today's number includes stations that reported no gasoline available and those assumed closed because EIA could not reach them after numerous attempts. Of the stations sampled, 72% reported gasoline available for sale (same as yesterday), 21% reported no gasoline supplies (same as yesterday), and 7% did not respond to attempts to contact them (same as yesterday).</p>

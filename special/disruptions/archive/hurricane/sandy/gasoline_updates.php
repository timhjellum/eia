<p>EIA's estimate of stations without gasoline available for sale is a composite number not designed to reflect the specific experience of more severely affected areas.  For more detailed information on gasoline availability, power outages, shelter and recovery centers, local emergency Twitter feeds, FEMA disaster declared areas and more, please go the  <a href="http://google.org/crisismap/2012-sandy">Google Emergency Map</a> available through the Department of Energy's website.</p>
<p>To develop the emergency survey, EIA used the representative sample of retail stations selling gasoline used in EIA's Form EIA-878, &quot;Motor Gasoline Price Survey&quot;. In the EIA-878 survey sample, the New York City area is defined by the New York-Northern New Jersey-Long Island, NY-NJ-CT Metropolitan Statistical Area (see Map). For further details on the methodology used to create the sample, see  <a href="http://www.eia.gov/petroleum/gasdiesel/gas_methodology.cfm">methodology</a>.</p>
     <p>Survey questions included the availability of electricity for operations as well as the availability of gasoline and diesel fuel. EIA was unable to contact some stations. EIA included stations in the nonresponse category as stations without gasoline available for sale. In fact, there could be many reasons EIA was unable to contact these stations, including the stations being closed for lack of power or fuel, the stations being too busy to answer the telephone, and a failure of the telephone system. In reviewing results through the week, EIA improved its ability to interpret the survey information. Today's final report uses only data based on that improved interpretation.</p>

<p>The tables show about 1.4 million barrels of product movement through these terminals, but that number may include double counting of movements between some of the terminals.  The data are very preliminary, and represent terminal operators' best information at the time.  However, both the pre-storm and post-storm data were collected using the same methodology and, as a result, the data sets should be comparable and indicative of the change in product flows. </p>
<p>It is important to point out areas normally served by the NYH terminals were also receiving some supplies through more distant terminals as the industry pursued workarounds to meet consumer needs to the best of their ability.  For this reason, the difference between the pre-storm and post-storm outflows from the NYH terminals is likely to overstate the impact on total product supplies available to consumers usually supplied through those terminals. </p>
<p>The data in Table 1 indicate some significant declines in ability to move product through the NYH hub.  Gasoline is the major product moving through these terminals.  After the storm, the terminals were receiving only 63% of the gasoline that they had been receiving before the storm, but were sending out 72% of the pre-storm volumes.  Note that pre-storm gasoline outflows were less than inflows, implying inventory building of that product.  But after the storm, outflows and inflows were fairly close.</p>
<p>Barge shipments have been most impacted by the storm.  As shown on Table 2, as of November 13, barge shipments of all products were running at 45% of pre-storm levels.  Barge shipments of gasoline were running at 54% of pre-storm levels and distillate shipments were even lower, only 46% of pre-storm barge shipments.  These data are indicative of the storm damage to dock facilities and time required to repair the facilities and return them to normal service.</p>
<p>EIA also collected data from tanker truck loading facilities (truck racks) at the terminals in the New York Harbor area.  These truck racks supply gasoline and distillate that is delivered to retail outlets and distributors in the New York metropolitan area.  By November 13, truck rack loadings of gasoline at these terminals had reached 90% of pre-storm levels and distillate loadings were 104% of pre-storm levels.</p>
<p>Some companies also reported inventories both before the hurricane and as of November 13.  By November 13, inventory levels at the reporting terminals had returned to normal levels.    </p>

      <h1>New York/New Jersey Intra Harbor Petroleum Supplies Following Hurricane Sandy:  Summary Of Impacts Through November 13, 2012</h1>
      <p><span class="dat">Release date: November 21, 2012 </span></p>
      <p>&nbsp;</p>
       <p>With the help of the National Petroleum Council and with the voluntary participation of the industry, the U.S. Energy Information Administration (EIA) conducted a survey to evaluate product flows in the New York Harbor (NYH) area both prior to Hurricane Sandy and as of Tuesday, November 13.  Terminals representing 98% of storage capacity in the NYH area participated in this effort.  EIA and Department of Energy staff have remained abreast of the evolving situation since that time.  However, this paper reports on the situation as of November 13, the last date for which data covering virtually all of the NYH petroleum terminal infrastructure are available.
      </p>
      <h3>Background</h3>
<p>At 8:00 p.m. EDT October 29, 2012, the National Hurricane Center reported that Sandy made landfall near Atlantic City, New Jersey, as a post-tropical cyclone.  The accompanying winds and flooding were devastating to many areas in the Northeast and damaged much of the energy infrastructure, including petroleum product supply and delivery systems around the NYH area.</p>
<p>The NYH area is a major distribution hub for petroleum delivery to consumer markets in New England, New York, and New Jersey.  The terminals in this area, with combined storage capacity of about 70 million barrels,  receive product via pipeline from refineries on the U.S. Gulf Coast, the Philadelphia area, and the two refineries located in northern New Jersey &mdash; the Phillips 66 Bayway (238,000 barrels per day) and the Hess Port Reading (70,000 barrels per day).  The terminals also receive product via tanker and barge, much of it imported from outside the United States.  Products from the terminals are then redistributed by barge and pipeline mainly to distribution terminals in New England, throughout the NYH area, up the Hudson River as far as Albany, and via pipeline to upstate New York. These distribution terminals also supply gasoline, heating oil, and diesel fuel to trucks for delivery to retail outlets and local distributors.</p>
<h3>Impacts of Hurricane Sandy</h3>
<p>The hurricane damaged much of the petroleum supply infrastructure in the NYH area, including both of the refineries in northern New Jersey and many of the terminals.  The damage to the refineries and the terminals significantly disrupted the supply chain. Immediately following the storm, the lack of commercial or generator power kept many terminals from operating.  With terminals nonoperational, product supply into the region stopped. Colonial Pipeline, which moves substantial volumes of petroleum products from the Gulf Coast to the New York Harbor area, was forced to stop delivering products to the NYH terminals. Colonial also had to slow product movement throughout the entire pipeline, which serves areas from the Gulf Coast, up the East Coast into New Jersey and New York.</p>
<p>Some NYH terminals were badly damaged, but others were able to return to full or partial operation using generator power after dock inspections, underwater surveys, and electrical equipment replacement was completed.  As terminals began operating, the Colonial Pipeline was able to normalize product flows into the NYH terminals, although the recovery has not been smooth.  For example, after commercial power had been restored to terminals and pipelines in the area, Colonial Pipeline and several terminals lost power a second time as utilities struggled to repair the commercial power infrastructure.</p>
<p>As of November 13, neither the Phillips 66 Bayway Refinery nor the Hess Port Reading refinery was operating, although the Hess refinery has more recently begun to operate, and Phillips has reported plans to be partially operational late this month.  As of November 13, most of the terminals in the NYH area had returned to at least partial operations, but some are still impaired.  Damage to electrical and other infrastructure inside several terminals was significant and repairs continue. In addition, damage to barge and tanker dock facilities has been limiting waterborne receipts and shipments.  Terminals representing 98% of storage capacity in the NYH area voluntarily reported information provided in Tables 1 and 2.</p>
